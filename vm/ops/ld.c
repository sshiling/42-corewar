#include "corewar.h"

void			op_ld(t_vm *vm, t_process *p, t_op *op)
{
	op->args[0] = get_arg(vm, p, op, 0);
	p->r[op->args[1]] = op->args[0];
	p->carry = !p->r[op->args[1]];
}
